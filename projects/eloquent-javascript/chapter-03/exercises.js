// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  // determine if num1 is smaller than num2
  if (num1 < num2) {
    // if num1 is smaller than num2 return num1
      return num1;
  } // else return num2
  else {
      return num2;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  // base case
  if (num === 0) {
      return true;
  } else if (num === 1) {
      return false;
      // recursive
  } else if (num < 0) {
      return isEven(-num);
  } else {
      return isEven(num - 2);
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  // create count variable
  let count = 0;
  // loop through string
  for (let i = 0; i < str.length; i++) {
    // determine if string character is equal to char
      if (str[i] === char) {
        // increment by 1
          count++;
      }
  }
  // return count
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  // create a count variable
  let count = 0;
  // loop through each character of the string
  for (let i = 0; i < str.length; i++) {
      // determine if the character is "B"
      if (str[i] === "B") {
          // if true increment the count
          count++;
      }
  }
  // return count
  return count;
}

